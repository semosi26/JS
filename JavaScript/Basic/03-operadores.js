// operadores aritmeticos
let a =5
let b =10
console.log(a+b)// suma
console.log(a-b)// resta 
console.log(a*b)// multiplicacion
console.log(a/b)// division
console.log(a%b)// modulo (resto de la division)
console.log(a**b)// potencia (a elevado a b)

console.log(a++);// postincremento (a=6)
console.log(a--);// postdecremento (a=5)
console.log(a)


// operadores de asignacion 
let myvar = 2;
console.log(myvar);
myvar +=2 // es igual que hacer  myvar = myvar + 2
console.log(myvar);
myvar -=2 
myvar *=2 
myvar /=2

// operadores de comparacion 
console.log(a>b)
console.log(a<b)
console.log(a==b) // igualdad por valor
console.log(a===6)// compara valor y tipo de dato   
console.log(a!==6)// compara valor y tipo de dato
console.log(a==='6')// compara valor y tipo de dato   


// operadores logicos

// and (&&)
console.log(5>10 && 15>10)
console.log(5<10 && 15<20)
console.log(5<10 && 15>20)

// or (||)
console.log(5>10 || 15>10)
console.log(5<10 || 15<20)
console.log(5<10 || 15>20)